package org.javaclass;

public class PublicVsStatic {
	
	// static method
  public static void mymethod() {
	   
	   System.out.println("this is static method no need to create object");
   }	
   
  
   
   //public method 
   
   public void publicmethod() {
	   System.out.println("we need to create object");
   }


}
